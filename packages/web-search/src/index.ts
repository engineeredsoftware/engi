// We support operating without an EXA credential by falling back to a mock client in
// development. Set ENGI_MOCK_EXA=true (or ENGI_MOCK_EXTERNAL=true) to force the mock.
type ExaClientLike = {
  searchAndContents: typeof Exa.prototype.searchAndContents;
  contents: typeof Exa.prototype.contents;
  findSimilar: typeof Exa.prototype.findSimilar;
};

class MockExaClient implements ExaClientLike {
  async searchAndContents(query: string): Promise<ExaSearchResponse> {
    return {
      results: [
        {
          title: `Mock result for "${query}"`,
          url: 'https://example.com/mock-result',
          score: 1,
          id: 'bitcode-mock-result',
          summary: 'Mock summary generated by Bitcode to unblock local development.',
          text: 'Mock body text generated by Bitcode.'
        }
      ],
      autopromptString: 'mock-autoprompt'
    };
  }

  async contents(ids: string[]): Promise<ExaContentsResponse> {
    return {
      results: ids.map((id, index) => ({
        title: `Mock contents for ${id}`,
        url: `https://example.com/mock-content/${index}`,
        score: 1,
        id
      }))
    };
  }

  async findSimilar(url: string): Promise<ExaSearchResponse> {
    return {
      results: [
        {
          title: `Mock similar result for ${url}`,
          url: `${url}?mock=1`,
          score: 1,
          id: 'bitcode-mock-similar'
        }
      ]
    };
  }
}

type MockReason = 'ENGI_MOCK_EXA' | 'ENGI_MOCK_EXTERNAL' | 'MISSING_API_KEY';

function parseBoolean(value: string | undefined): boolean {
  if (typeof value !== 'string') return false;
  const normalized = value.trim().toLowerCase();
  return normalized === 'true' || normalized === '1' || normalized === 'yes' || normalized === 'y' || normalized === 'on';
}

function getMockDecision(apiKey: string | undefined): { shouldMock: true; reason: MockReason } | { shouldMock: false } {
  if (parseBoolean(process.env.ENGI_MOCK_EXA)) {
    return { shouldMock: true, reason: 'ENGI_MOCK_EXA' };
  }

  if (parseBoolean(process.env.ENGI_MOCK_EXTERNAL)) {
    return { shouldMock: true, reason: 'ENGI_MOCK_EXTERNAL' };
  }

  if (!apiKey && process.env.NODE_ENV !== 'production') {
    return { shouldMock: true, reason: 'MISSING_API_KEY' };
  }

  return { shouldMock: false };
}

function createExaClient(): ExaClientLike {
  const apiKey = process.env.EXA_API_KEY ?? process.env.EXASEARCH_API_KEY;

  const decision = getMockDecision(apiKey);

  if (decision.shouldMock) {
    const reason =
      decision.reason === 'MISSING_API_KEY'
        ? 'missing API key'
        : `explicit mock flag (${decision.reason === 'ENGI_MOCK_EXA' ? 'ENGI_MOCK_EXA' : 'ENGI_MOCK_EXTERNAL'})`;

    log('Using mock Exa client for Bitcode ChatGPT App', 'warn', {
      reason,
      note: 'Set ENGI_MOCK_EXA=false and provide EXA_API_KEY to hit the real service.'
    });
    return new MockExaClient();
  }

  if (!apiKey) {
    throw new Error(
      'EXA_API_KEY (or EXASEARCH_API_KEY) must be provided. Set ENGI_MOCK_EXA=true to use the mock client in development.'
    );
  }

  return new Exa(apiKey);
}

// Initialize Exa client once per module load.
const exa = createExaClient();

