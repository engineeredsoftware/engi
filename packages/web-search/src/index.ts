export async function search(
  query: string,
  options: SearchOptions = {}
): Promise<ExaSearchResponse> {
  logApiCall('search', { query, ...options });

  try {
    return await exa.searchAndContents(query, {
      useAutoprompt: options.useAutoprompt ?? true,
      type: options.type ?? 'neural',
      category: options.category,
      numResults: options.numResults ?? 10,
      includeDomains: options.includeDomains,
      excludeDomains: options.excludeDomains,
      startCrawlDate: options.startCrawlDate,
      endCrawlDate: options.endCrawlDate,
      startPublishedDate: options.startPublishedDate,
      endPublishedDate: options.endPublishedDate,
      includeText: options.includeText,
      excludeText: options.excludeText,
      text: options.contents?.text?.maxCharacters !== undefined,
      highlights: options.contents?.highlights !== undefined,
      summary: options.contents?.summary !== undefined
    });
  } catch (error) {
    log('Exa API search failed', 'error', {
      error: error instanceof Error ? error.message : String(error),
      query,
      options
    });
    throw error;
  }
}

